def AddCal(n1, n2):
    print(f'덧셈: {n1} + {n2} = {round(n1 + n2, 2)}')


def SubCal(n1, n2):
    print(f'뺄셈: {n1} - {n2} = {round(n1 - n2, 2)}')


def MulCal(n1, n2):
    print(f'곱셈: {n1} * {n2} = {round(n1 * n2, 2)}')


def DivCal(n1, n2):
    print(f'나눗셈: {n1} / {n2} = {round(n1 / n2, 2)}')