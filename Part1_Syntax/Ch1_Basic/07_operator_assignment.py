'''
# [할당 & 복합 연산자] : =, +=, -=, *=, /=, %=, //=, **=
'''

num = 10
num += 3
print(f'num : {num}')
num = 10
num -= 3
print(f'num : {num}')
num = 10
num *= 3
print(f'num : {num}')
num = 10
num /= 3
print(f'num : {num}')
num = 10
num %= 3
print(f'num : {num}')
num = 10
num //= 3
print(f'num : {num}')
num = 2
num **= 3
print(f'num : {num}')


